package com.speed.user.service;

public class Calculator {

    //SUM
    public int doSum(int a , int b){
        return a + b;
    }

    //PRODUCT
    public int doProduct(int a, int b){
        return  a * b;
    }

    public Boolean compareTwoNums(int a , int b){
        return a==b;
    }

}
